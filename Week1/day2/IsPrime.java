package Week1.day2;


public class IsPrime {
	
	public static void main(String[] args) {
		
		for (int i = 2; i <=10; i++) {
			
			if(i%2==1) {
				System.out.println("The number is not a prime");
				
				System.out.println(i);
				
			}
				else {
					System.out.println("The number is a prime");
					
					System.out.println(i);
					
				}
			
		}
	}
	

	
}
